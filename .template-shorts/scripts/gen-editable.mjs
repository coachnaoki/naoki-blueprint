#!/usr/bin/env node
/**
 * src/telopData.ts → src/EditableTelops.ts (schema + meta) と src/Root.tsx (defaultProps inline) を自動生成
 *
 * 使い方:
 *   node scripts/gen-editable.mjs
 *
 * 仕組み:
 *   - Remotion Studio の Visual Editing 機能（codemod ベース）を使う
 *   - <Composition schema={...} defaultProps={...inline...} /> 形式
 *   - defaultProps は inline でないと Studio が "Could not find or extract defaultProps" エラーになる
 *   - 編集→💾保存ボタンで Root.tsx の defaultProps が codemod で書き換わる
 *
 * 出力ファイル:
 *   1. src/EditableTelops.ts: zod schema + 不変メタデータ (text/template/emphasisWord)
 *   2. src/Root.tsx: <Composition> に schema + defaultProps を inline 展開
 *
 * 注意:
 *   - src/telopData.ts を更新したら必ず再生成
 *   - SE タイミングは元の telopData ベース（PoC スコープ外）
 *   - Root.tsx は本スクリプトで毎回再生成される（手書きカスタマイズは保持されない）
 */

import fs from "node:fs";
import path from "node:path";
import url from "node:url";

const __dirname = path.dirname(url.fileURLToPath(import.meta.url));
const telopPath = path.join(__dirname, "..", "src", "telopData.ts");
const editableTelopsPath = path.join(__dirname, "..", "src", "EditableTelops.ts");
const rootPath = path.join(__dirname, "..", "src", "Root.tsx");

const src = fs.readFileSync(telopPath, "utf-8");

const entryRegex =
  /\{\s*text:\s*("(?:[^"\\]|\\.)*"),\s*startFrame:\s*(\d+),\s*endFrame:\s*(\d+),\s*template:\s*("[^"]+")(,\s*emphasisWord:\s*((?:"[^"]*"|\[[^\]]+\])))?\s*\}/g;

const entries = [];
let match;
while ((match = entryRegex.exec(src)) !== null) {
  entries.push({
    textLiteral: match[1],
    startFrame: Number(match[2]),
    endFrame: Number(match[3]),
    templateLiteral: match[4],
    emphasisWordLiteral: match[6] || null,
  });
}

if (entries.length === 0) {
  console.error("✗ telopData エントリが見つかりませんでした。");
  process.exit(1);
}

const maxFrame = Math.max(...entries.map((e) => e.endFrame));
const sliderMax = Math.ceil((maxFrame + 100) / 100) * 100;

/**
 * テロップごとの key 名（Studio Props パネルのヘッダー）
 */
const buildKey = (textLiteral, idx) => {
  const text = JSON.parse(textLiteral);
  const cleaned = text.replace(/\n/g, " ");
  const truncated = cleaned.length > 18 ? cleaned.slice(0, 18) + "…" : cleaned;
  return `[t${String(idx).padStart(2, "0")}] ${truncated}`;
};

// === Step 1: src/EditableTelops.ts を生成 ===
const schemaLines = entries.map((t, i) => {
  const key = JSON.stringify(buildKey(t.textLiteral, i));
  return `  ${key}: telopRangeSchema,`;
});

const metaLines = entries.map((t, i) => {
  const key = JSON.stringify(buildKey(t.textLiteral, i));
  const parts = [
    `text: ${t.textLiteral}`,
    `template: ${t.templateLiteral}`,
  ];
  if (t.emphasisWordLiteral) {
    parts.push(`emphasisWord: ${t.emphasisWordLiteral}`);
  }
  return `  ${key}: { ${parts.join(", ")} },`;
});

const editableTelopsContent = `// 🤖 Auto-generated by scripts/gen-editable.mjs
// 直接編集禁止。src/telopData.ts を編集後、'node scripts/gen-editable.mjs' で再生成。
// 生成日時: ${new Date().toISOString()}
// テロップ数: ${entries.length}

import { z } from "zod";
import { type TelopEntry } from "./telopData";
import { type TemplateName } from "./templateConfig";

/**
 * テロップ1個分の編集対象（start / end のみ）
 */
const telopRangeSchema = z.object({
  start: z.number().int().min(0).max(${sliderMax}),
  end: z.number().int().min(0).max(${sliderMax}),
});

/**
 * 全テロップを keyed object で持つスキーマ。
 * Studio Props パネルでテロップ名がそのまま折りたたみヘッダーになる。
 */
export const editableTelopsSchema = z.object({
${schemaLines.join("\n")}
});

/**
 * MainComposition の Props スキーマ
 */
export const mainCompositionSchema = z.object({
  telops: editableTelopsSchema,
});

export type MainCompositionProps = z.infer<typeof mainCompositionSchema>;

/**
 * テロップの不変メタデータ（text / template / emphasisWord）
 * Visual Editing で編集可能なのは start/end のみ。
 * 残りはこのマップを参照して MainComposition で結合する。
 */
export const telopMeta: Record<
  string,
  { text: string; template: TemplateName; emphasisWord?: string | string[] }
> = {
${metaLines.join("\n")}
};

// 既存テストとの互換維持: TelopEntry 型のみ再 export
export type { TelopEntry };
`;

fs.writeFileSync(editableTelopsPath, editableTelopsContent);
console.log(`✓ ${path.relative(process.cwd(), editableTelopsPath)} を生成`);

// === Step 2: src/Root.tsx を生成（defaultProps inline 展開） ===
// 既存 Root.tsx から durationInFrames / fps / width / height を保持
// （step05 で生成された値を尊重する）
const existingRoot = fs.existsSync(rootPath) ? fs.readFileSync(rootPath, "utf-8") : "";
const pickNumber = (regex, fallback) => {
  const m = existingRoot.match(regex);
  return m ? m[1] : fallback;
};
const durationInFrames = pickNumber(/durationInFrames=\{(\d+)\}/, "3030");
const fps = pickNumber(/fps=\{(\d+)\}/, "30");
const width = pickNumber(/width=\{(\d+)\}/, "1080");
const height = pickNumber(/height=\{(\d+)\}/, "1920");

const defaultPropsLines = entries.map((t, i) => {
  const key = JSON.stringify(buildKey(t.textLiteral, i));
  return `        ${key}: { start: ${t.startFrame}, end: ${t.endFrame} },`;
});

const rootContent = `// 🤖 Auto-generated by scripts/gen-editable.mjs
// 直接編集禁止。src/telopData.ts を編集後、'node scripts/gen-editable.mjs' で再生成。
// ただし Studio の💾保存ボタンによる更新は許可（codemod が defaultProps を書き換える）。
// 生成日時: ${new Date().toISOString()}

import "./index.css";
import { Composition } from "remotion";
import { MainComposition } from "./MainComposition";
import { mainCompositionSchema } from "./EditableTelops";

export const RemotionRoot: React.FC = () => {
  return (
    <Composition
      id="MainVideo"
      component={MainComposition}
      durationInFrames={${durationInFrames}}
      fps={${fps}}
      width={${width}}
      height={${height}}
      schema={mainCompositionSchema}
      defaultProps={{
        telops: {
${defaultPropsLines.join("\n")}
        },
      }}
    />
  );
};
`;

fs.writeFileSync(rootPath, rootContent);
console.log(`✓ ${path.relative(process.cwd(), rootPath)} を生成`);
console.log("");
console.log(`  - ${entries.length} テロップを inline 展開`);
console.log(`  - schema: keyed object (テロップ名がヘッダー)`);
console.log(`  - スライダー範囲: 0 〜 ${sliderMax}`);
console.log("");
console.log("Studio リロードで Props タブを確認してください。");
